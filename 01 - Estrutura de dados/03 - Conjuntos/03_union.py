# Union - União de conjuntos
# A união de conjuntos é uma operação que retorna um novo conjunto com todos os elementos que pertencem a pelo menos um dos conjuntos, sem repetir os conjuntos que aprece nos dois conjuntos.

conjunto_a = {1, 2}
conjunto_b = {3, 4}

resultado = conjunto_a.union(conjunto_b)
print(resultado)
